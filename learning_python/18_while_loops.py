#bare basics of while loops
#loop condition is going to keep looping through the code...
#...as long as the condition is true within while loop.
i = 1
while i <= 10: #python checks the condition first. is i less than 10? 
    print(i) #executes the code
    i = i + 1 #(i += 1 does the same job) adds the number and goes back to start until i is <= 10.
    
print("Done with loop") #until it is done with the loop.

